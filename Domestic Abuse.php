      <h1 style="color:red;"> Domestic Abuse</h1>
          <div style="margin:50px 200px 50px 200px;text-align:justify;">
        <h4> What is domestic abuse </h4>
        <p> Domestic abuse is any type of controlling, bullying, threatening or violent behaviour between people in a relationship. But it isn’t just physical violence – domestic abuse includes emotional, physical, sexual, financial or psychological abuse.</p>
       <p>Abusive behaviour can occur in any relationship. It can continue even after the relationship has ended. Both men and women can be abused or abusers.</p>
       <p>Domestic abuse can seriously harm children and young people. Witnessing domestic abuse is child abuse, and teenagers can suffer domestic abuse in their relationships. </p>
       <br>
       <h4>Types of domestic abuse</h4>
       <p>Domestic abuse can include:</p>
       <li>sexual abuse and rape (including within a relationship)</li>
       <li>punching, kicking, cutting, hitting with an object</li>
       <li>withholding money or preventing someone from earning money</li>
       <li>taking control over aspects of someone's everyday life, which can include where they go and what they wear</li>
       <li>not letting someone leave the house</li>
       <li>reading emails, text messages or letters</li>
       <li>threatening to kill or harm them, a partner, another family member or pet.</li>
       <br>
       <h6>Children and young people witnessing domestic abuse</h6>
       <p>Witnessing domestic abuse is really distressing and scary for a child, and causes serious harm. Children living in a home where domestic abuse is happening are at risk of other types of abuse too. Children can experience domestic abuse or violence in lots of different ways. They might:</p>
       <li>see the abuse</li>
       <li>hear the abuse from another room</li>
       <li>see a parent's injuries or distress afterwards</li>
       <li>be hurt by being nearby or trying to stop the abuse</li>
       <br>
       <h6>Teenagers experiencing domestic abuse</h6>
       <p>Domestic abuse can happen in any relationship, and it affects young people too. </p>
       <p>They may not realise that what's happening is abuse. Even if they do, they might not tell anyone about it because they're scared of what will happen, or ashamed about what people will think.</p>
